@app.route("/")
def index():
    """
    Serve the main web page.
    """
    return render_template("index.html")


@app.route("/api/careers")
def api_careers():
    """
    API endpoint: Return available careers from the RDF graph.

    Used by the frontend to populate the career dropdown.
    """
    graph = load_rdf_graph()

    if graph is None:
        return jsonify({"error": "RDF graph not found. Please run py main.py first."}), 500

    careers = get_available_careers(graph)

    return jsonify({"careers": careers})


@app.route("/api/skills")
def api_skills():
    """
    API endpoint: Return all official skill names.

    Used by the frontend for autocomplete suggestions.
    """
    skill_names, alias_map = load_skill_aliases_from_xml()

    return jsonify({
        "skill_names": skill_names,
        "alias_map": alias_map
    })


@app.route("/api/validate-skill")
def api_validate_skill():
    """
    API endpoint: Validate a single skill input and return the
    resolved official name, or a fuzzy suggestion if applicable.

    Used by the frontend tag input for real-time validation.
    """
    raw_input = request.args.get("skill", "").strip().lower()

    if not raw_input:
        return jsonify({"status": "empty"})

    skill_names, alias_map = load_skill_aliases_from_xml()

    official_skill_lookup = {name.lower(): name for name in skill_names}
    official_skill_lowercase = list(official_skill_lookup.keys())

    # Exact alias match
    if raw_input in alias_map:
        return jsonify({
            "status": "matched",
            "official_name": alias_map[raw_input]
        })

    # Fuzzy match
    possible_matches = get_close_matches(
        raw_input,
        official_skill_lowercase,
        n=1,
        cutoff=0.7
    )

    if possible_matches:
        suggested_skill = official_skill_lookup[possible_matches[0]]
        return jsonify({
            "status": "suggestion",
            "original": raw_input,
            "suggested": suggested_skill
        })

    return jsonify({
        "status": "unknown",
        "original": raw_input
    })


@app.route("/api/analyze", methods=["POST"])
def api_analyze():
    """
    API endpoint: Run the full skill gap analysis.

    Expects JSON body:
    {
        "career_uri": "http://example.org/career-skill#DataAnalyst",
        "skills": "pyhton, sql, ml"
    }

    Returns the complete analysis result as JSON.
    """
    data = request.get_json()

    if not data:
        return jsonify({"error": "No input data provided."}), 400

    career_uri = data.get("career_uri", "")
    skills_input = data.get("skills", "")

    if not career_uri:
        return jsonify({"error": "Please select a target career."}), 400

    if not skills_input.strip():
        return jsonify({"error": "Please enter at least one skill."}), 400

    # Load RDF graph
    graph = load_rdf_graph()

    if graph is None:
        return jsonify({"error": "RDF graph not found. Please run py main.py first."}), 500

    # Load skill aliases for input normalisation
    skill_names, alias_map = load_skill_aliases_from_xml()

    # Normalise user input
    confirmed_skills, unknown_skills, corrections = normalise_skills_for_web(
        skills_input,
        skill_names,
        alias_map
    )

    # Get career details
    careers = get_available_careers(graph)
    selected_career = None

    for career in careers:
        if career["career_uri"] == career_uri:
            selected_career = career
            break

    if selected_career is None:
        return jsonify({"error": "Selected career not found."}), 400

    # Run SPARQL-powered analysis
    required_skills = get_required_skills_for_career(graph, career_uri)

    matched_skills, missing_skills, readiness_score = analyse_skill_gap(
        required_skills,
        confirmed_skills
    )

    recommended_courses = get_courses_for_missing_skills(graph, missing_skills)

    alternative_careers = suggest_alternative_careers(
        graph,
        career_uri,
        confirmed_skills
    )

    grouped_missing = group_missing_skills_by_priority(missing_skills)

    # Return full analysis result as JSON
    return jsonify({
        "selected_career": selected_career,
        "confirmed_skills": confirmed_skills,
        "unknown_skills": unknown_skills,
        "corrections": corrections,
        "required_skills": required_skills,
        "matched_skills": matched_skills,
        "missing_skills": missing_skills,
        "grouped_missing": grouped_missing,
        "readiness_score": readiness_score,
        "recommended_courses": recommended_courses,
        "alternative_careers": alternative_careers
    })


