# SPARQL Query Functions
# ============================================================


def get_available_careers(graph):
    """
    Retrieve available careers from the RDF graph using SPARQL.
    """
    query = """
    PREFIX ex: <http://example.org/career-skill#>

    SELECT ?career ?careerName ?sourceTitle ?sourceCode ?careerLevel
    WHERE {
        ?career a ex:Career .
        ?career ex:careerName ?careerName .
        ?career ex:sourceOccupationTitle ?sourceTitle .
        ?career ex:sourceOccupationCode ?sourceCode .
        ?career ex:careerLevel ?careerLevel .
    }
    ORDER BY ?careerName
    """

    careers = []

    for row in graph.query(query):
        careers.append({
            "career_uri": str(row.career),
            "career_name": str(row.careerName),
            "source_title": str(row.sourceTitle),
            "source_code": str(row.sourceCode),
            "career_level": str(row.careerLevel)
        })

    return careers


def get_required_skills_for_career(graph, career_uri):
    """
    Retrieve required skills for the selected career using SPARQL.
    """
    query = """
    PREFIX ex: <http://example.org/career-skill#>

    SELECT ?skill ?skillName ?skillCategory ?priority
    WHERE {
        <CAREER_URI> ex:requiresSkill ?skill .

        ?skill ex:skillName ?skillName .

        ?skill ex:skillCategory ?skillCategory .

        ?requirement a ex:SkillRequirement .
        ?requirement ex:requirementForCareer <CAREER_URI> .
        ?requirement ex:requiresSkill ?skill .
        ?requirement ex:priority ?priority .
    }
    ORDER BY ?priority ?skillName
    """.replace("CAREER_URI", career_uri)

    required_skills = []

    for row in graph.query(query):
        required_skills.append({
            "skill_uri": str(row.skill),
            "skill_name": str(row.skillName),
            "category": str(row.skillCategory),
            "priority": str(row.priority)
        })

    return required_skills


def analyse_skill_gap(required_skills, confirmed_skills):
    """
    Compare confirmed user skills with SPARQL-retrieved required skills.
    """
    matched_skills = []
    missing_skills = []

    for skill in required_skills:
        if skill["skill_name"] in confirmed_skills:
            matched_skills.append(skill)
        else:
            missing_skills.append(skill)

    total_required = len(required_skills)
    total_matched = len(matched_skills)

    if total_required > 0:
        readiness_score = round((total_matched / total_required) * 100)
    else:
        readiness_score = 0

    return matched_skills, missing_skills, readiness_score


def get_courses_for_missing_skills(graph, missing_skills):
    """
    Retrieve recommended courses using SPARQL.

    This query uses the SPARQL VALUES clause to inject the missing skills
    directly into the query, filtering courses at the graph database level.
    """
    if not missing_skills:
        return []

    skill_names_str = " ".join([f'"{skill["skill_name"]}"' for skill in missing_skills])

    query = f"""
    PREFIX ex: <http://example.org/career-skill#>

    SELECT ?course ?courseName ?skillName ?prerequisiteName
    WHERE {{
        VALUES ?skillName {{ {skill_names_str} }}

        ?course a ex:Course .
        ?course ex:courseName ?courseName .
        ?course ex:teachesSkill ?skill .
        ?skill ex:skillName ?skillName .

        OPTIONAL {{
            ?course ex:prerequisiteSkill ?prerequisite .
            ?prerequisite ex:skillName ?prerequisiteName .
        }}
    }}
    ORDER BY ?courseName ?skillName
    """

    recommended = {}

    for row in graph.query(query):
        skill_name = str(row.skillName)
        course_uri = str(row.course)
        course_name = str(row.courseName)

        if course_uri not in recommended:
            recommended[course_uri] = {
                "course_name": course_name,
                "teaches_missing_skills": [],
                "prerequisite_skills": []
            }

        if skill_name not in recommended[course_uri]["teaches_missing_skills"]:
            recommended[course_uri]["teaches_missing_skills"].append(skill_name)

        if row.prerequisiteName:
            prerequisite_name = str(row.prerequisiteName)

            if prerequisite_name not in recommended[course_uri]["prerequisite_skills"]:
                recommended[course_uri]["prerequisite_skills"].append(prerequisite_name)

    return list(recommended.values())


def suggest_alternative_careers(graph, selected_career_uri, confirmed_skills):
    """
    Retrieve career-skill relationships using SPARQL and calculate
    match percentage for alternative careers.

    This query filters out the selected target career at the SPARQL graph level.
    """
    query = f"""
    PREFIX ex: <http://example.org/career-skill#>

    SELECT ?career ?careerName ?skillName
    WHERE {{
        ?career a ex:Career .
        ?career ex:careerName ?careerName .

        FILTER(?career != <{selected_career_uri}>)

        ?career ex:requiresSkill ?skill .
        ?skill ex:skillName ?skillName .
    }}
    ORDER BY ?careerName ?skillName
    """

    career_skill_map = {}

    for row in graph.query(query):
        career_uri = str(row.career)
        career_name = str(row.careerName)
        skill_name = str(row.skillName)

        if career_uri not in career_skill_map:
            career_skill_map[career_uri] = {
                "career_name": career_name,
                "required_skills": []
            }

        career_skill_map[career_uri]["required_skills"].append(skill_name)

    alternatives = []

    for career_uri, career_data in career_skill_map.items():
        required_skills = career_data["required_skills"]
        matched_count = 0

        for skill_name in required_skills:
            if skill_name in confirmed_skills:
                matched_count += 1

        if required_skills:
            match_score = round((matched_count / len(required_skills)) * 100)
        else:
            match_score = 0

        alternatives.append({
            "career_name": career_data["career_name"],
            "match_score": match_score
        })

    alternatives.sort(key=lambda item: item["match_score"], reverse=True)

    return alternatives


def group_missing_skills_by_priority(missing_skills):
    """
    Group missing skills into High, Medium, and Low priority.
    """
    grouped = {
        "High": [],
        "Medium": [],
        "Low": []
    }

    for skill in missing_skills:
        priority = skill["priority"]

        if priority in grouped:
            grouped[priority].append(skill)
        else:
            grouped["Low"].append(skill)

    return grouped


